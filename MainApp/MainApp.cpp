#include "MainApp_PCH.h"
#include "MainApp/MainApp.h"

#include "Game/DB/DBManager.h"
#include "Game/entity/EntityLibrary.h"
#include "MDGame/MDGame.h"

///////////////////////////////////////////////////////////////////////////
CMainApp::CMainApp()
    : m_game( NULL )
{
}

///////////////////////////////////////////////////////////////////////////
CMainApp::~CMainApp()
{
    delete m_game;
    m_game = NULL;
}

///////////////////////////////////////////////////////////////////////////
void CMainApp::Init()
{
    m_game = new CMDGame();
    m_game->Init();
}

///////////////////////////////////////////////////////////////////////////
void CMainApp::Shutdown()
{
    m_game->Shutdown();
}

///////////////////////////////////////////////////////////////////////////
bool CMainApp::Run()
{
    return m_game->Run();
}
